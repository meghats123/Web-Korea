                      <p class="card-text">Korea is a small peninsula located between China and Japan. As a result of the Allied victory in World War II in 1945, Korea was divided into two sovereign states with different political ideologies: the Republic of Korea to the south and the Democratic People’s Republic of Korea to the north. This ended Japan’s 35 year rule of Korea.</p>
                      <h4>Language</h4>
<p >Hangul is the name of the Korean alphabet. Although the spelling, alphabet and vocabulary differ slightly between the two countries, Korean is the official language of both South Korea and North Korea. Linguists believe the Korean language is a member of the Altaic family of languages, which originated in northern Asia.</p>

  <p>There are five major dialects in South Korea and one in North Korea. Despite differences in the dialects, speakers from different areas can understand each other.The Korean alphabet is easy to learn and Korea enjoys one of the highest literacy rates in the world.</p>
      <h4>Family Structure</h4>
      <p>Family is the most important part of Korean society and the father is the leader of the family. They also believe in a hierarchical structure and children help their parents and must obey them and show respect to the elders. As Confucius teachings have a deep influence on Koreans lives, they believe in duty, loyalty, honor and sincerity. That’s why children live with their parents after they became adults as they believe it is their duty to take care of their parents.</p>
